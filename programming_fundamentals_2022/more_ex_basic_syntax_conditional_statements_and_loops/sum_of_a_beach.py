some_string = input()
lower_case_string = some_string.lower()
counter_sand = lower_case_string.count('sand')
counter_sun = lower_case_string.count('sun')
counter_fish = lower_case_string.count('fish')
counter_water = lower_case_string.count('water')
total_count = counter_sand + counter_sun + counter_fish + counter_water
print(total_count)




